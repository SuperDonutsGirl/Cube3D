#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	unsigned int	len;
	char			*mapi;

	if (!s || !f)
		return (0);
	i = 0;
	len = ft_strlen(s);
	mapi = (char *)malloc(sizeof(char) * len + 1);
	if (!mapi)
		return (0);
	while (s[i])
	{
		mapi[i] = f(i, s[i]);
		i++;
	}
	mapi[i] = '\0';
	return (mapi);
}
